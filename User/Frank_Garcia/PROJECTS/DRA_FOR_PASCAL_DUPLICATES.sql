BEGIN TRAN

SELECT
	DRA.APS_ID
	,DRA.SCHOOL_YEAR
	,DRA.TEST_WINDOW
	,DRA.TEST_TYPE
	,DRA.HOME_LANGUAGE
FROM
	db_DRA.dbo.DRA_FOR_PASCAL AS DRA
	INNER JOIN
	AIMS.dbo.DRA_DUPS AS DUP
	ON DRA.APS_ID = DUP.APS_ID
	AND DRA.SCHOOL_YEAR = DUP.SCHOOL_YEAR
	AND DRA.TEST_WINDOW = DUP.TEST_WINDOW
			
ORDER BY APS_ID, SCHOOL_YEAR, TEST_TYPE

ROLLBACK