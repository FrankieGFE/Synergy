




SELECT
	ORG.ORGANIZATION_NAME
	,PER.FIRST_NAME
	,PER.LAST_NAME
	,STU.SIS_NUMBER
	,STU.STATE_STUDENT_NUMBER
	,ENR.ENTER_DATE
	,ENR.LEAVE_DATE 
FROM
	APS.EnrollmentsAsOf ('2017-11-14')  ENR  --- Change to 201/-11-14 for enrollments greater than 2017-08-14
	JOIN
	REV.EPC_STU STU
	ON STU.STUDENT_GU = ENR.STUDENT_GU
	JOIN
	REV.REV_ORGANIZATION_YEAR OYR
	ON OYR.ORGANIZATION_YEAR_GU = ENR.ORGANIZATION_YEAR_GU
	JOIN REV.REV_ORGANIZATION ORG
	ON ORG.ORGANIZATION_GU = OYR.ORGANIZATION_GU
	JOIN 
	REV.REV_PERSON PER
	ON PER.PERSON_GU = STU.STUDENT_GU
WHERE 1 = 1
	--AND ENTER_DATE = '2017-08-14 00:00:00' AND LEAVE_DATE = '2017-08-14 00:00:00'   --- For enrollments with equal start and end dates
	AND ENTER_DATE > GETDATE()  --- For enrollments greater than 2017-08-14
	AND ORGANIZATION_NAME NOT IN ('Coyote Willow Family School','Desert Willow Family School','College and Career High School')

	----EXCLUDE 591, DESERT WILLOW, COYOTE WILLOW 
ORDER BY ENTER_DATE DESC
