SELECT  DISTINCT
		BS.SIS_NUMBER
		--,bs.STUDENT_GU
		--,GRADE
		,BS.STATE_STUDENT_NUMBER
		,GRADE.VALUE_DESCRIPTION AS GRADE
		,PER.LAST_NAME
		,PER.FIRST_NAME
		,BS.RESOLVED_RACE
		,BS.LUNCH_STATUS
		,CASE WHEN AVID.STUDENT_GU IS NOT NULL THEN 'Yes' ELSE 'No'
		END AS AVID
		--,ENR.LEAVE_DATE
		--,ORG.ORGANIZATION_NAME
		--,SCH.SCHOOL_TYPE
FROM
	APS.EnrollmentsForYear('BCFE2270-A461-4260-BA2B-0087CB8EC26A') ENR
	JOIN
	APS.BasicStudentWithMoreInfo BS
	ON BS.STUDENT_GU = ENR.STUDENT_GU
	JOIN
	REV.REV_PERSON PER
	ON PER.PERSON_GU = ENR.STUDENT_GU
	LEFT JOIN 
	rev.SIF_22_Common_GetLookupValues('K12', 'GRADE') grade 
	ON grade.VALUE_CODE = ENR.GRADE
	LEFT JOIN
	(
		SELECT DISTINCT STUDENT_GU
		FROM
			APS.ScheduleForYear('BCFE2270-A461-4260-BA2B-0087CB8EC26A') SCHED
		WHERE 1 = 1
		AND SCHED.COURSE_GU
			IN
			(
			'BE883D13-6556-456F-82E6-0B1D4774061F',
			'F1E8E5AD-D4A8-48EC-8B03-B4663D0E6030',
			'6FBAD9AD-79D3-4C8D-9491-733ED990EC09',
			'85CF5F53-9DCB-4239-AB9A-37F75BFF0A09',
			'D6BC47BA-95F7-4402-8E05-0ACE774D42BD',
			'072683DE-3722-4CBD-95D9-00208D46FC7A',
			'8DE749E9-72FA-4B30-8D3F-33E8A60E6F45',
			'556483CC-5136-435D-9214-16E192BDCD0D',
			'0191D00F-4BC8-4986-8609-66BF76CB874B',
			'B2E55E95-ED9E-4B11-A57C-1DB205C1F463',
			'9081F25B-82C7-4F59-9D3A-24365D6B4132',
			'041510AF-68EF-4BDC-8D4A-D9CD0CF8B68C',
			'27667CE1-CEE1-42D3-8F39-8A6A3C32E2B8',
			'4EC55569-67F2-4B8F-B6F4-62871308B5F9',
			'12F0EF74-DED6-4667-8720-677697351FFD'
			) 
	) AS AVID
	ON AVID.STUDENT_GU = ENR.STUDENT_GU
	JOIN
	REV.REV_ORGANIZATION ORG
	ON ORG.ORGANIZATION_GU = ENR.ORGANIZATION_GU
	JOIN
	REV.EPC_SCH SCH
	ON SCH.ORGANIZATION_GU = ENR.ORGANIZATION_GU
WHERE 1 = 1
	AND (ORG.ORGANIZATION_NAME NOT LIKE '%ELEMENTARY%' AND ORG.ORGANIZATION_NAME NOT LIKE '%MIDDLE%' AND ORG.ORGANIZATION_NAME != 'Desert Willow Family School' AND ORG.ORGANIZATION_NAME != 'George I. Sanchez Collaborative Community School')
	AND ORG.ORGANIZATION_NAME NOT IN ('Central NM Comm College','Continuation School','Highland Autism Center Annex','New Mexico School for the Deaf','NM School For The Blind & Visually Impaired','NM School For The Blind & Visually Impaired','Private School','Special Education Department','Special Education Department','Title One School','Transition Services','University of NM','Pre-Enrollment','HOMEBOUND','HOME OR HOSPITAL INSTRUCTION')
	AND GRADE NOT IN ('180','170','160','150','140','130','120','110')

order by GRADE.VALUE_DESCRIPTION 


