

SELECT SIS_NUMBER, LEVELS.VALUE_DESCRIPTION AS LEVEL_INTEGRATION
,LEVEL_INTEGRATION 
FROM 

rev.UD_SPED_SERVICE_LEVEL AS SPED
INNER JOIN
rev.EPC_STU AS STU
ON
SPED.STUDENT_GU = STU.STUDENT_GU
INNER JOIN 
APS.LookupTable ('K12.SpecialEd.IEP', 'LEVEL_INTEGRATION') AS LEVELS
ON
LEVELS.VALUE_CODE = SPED.LEVEL_INTEGRATION

where sis_number = '970095723'