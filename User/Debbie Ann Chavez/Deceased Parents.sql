

SELECT DISTINCT SIS_NUMBER, PARENT_LAST_NAME, PARENT_FIRST_NAME, DECEASED, LIVES_WITH FROM 
rev.REV_PERSON AS PERS
INNER JOIN
rev.EPC_STU_PARENT AS PAR
ON
PERS.PERSON_GU = PAR.PARENT_GU
INNER JOIN
(SELECT 
			stu.STUDENT_GU

		,PARENT_GU
		,PPER.LAST_NAME AS PARENT_LAST_NAME
		,PPER.FIRST_NAME AS PARENT_FIRST_NAME
		FROM rev.EPC_STU stu
		JOIN rev.EPC_STU_PARENT spar ON spar.STUDENT_GU = stu.STUDENT_GU 
		--AND spar.LIVES_WITH = 'Y'
		JOIN rev.REV_PERSON pper     ON pper.PERSON_GU  = spar.PARENT_GU
) AS STUPAR

ON
STUPAR.PARENT_GU = PAR.PARENT_GU

INNER JOIN
rev.EPC_STU AS STU
ON
STUPAR.STUDENT_GU = STU.STUDENT_GU

WHERE
DECEASED = 'Y'