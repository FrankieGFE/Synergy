(
SELECT 
	CASE WHEN LEFT(STATE_COURSE_CODE,4) BETWEEN '1271' AND '1274' AND QUALIFIED = 'N' THEN 'DELETE' ELSE '' END AS [FLAG]
	,EXCEPTIONS.*
FROM 
(

SELECT BEP.* FROM 

(
SELECT DISTINCT STUDENT_GU
FROM 
	dbo.BEPModelsAndHours
WHERE
QUALIFIED = 'Y'

) AS T1

INNER JOIN 
(
SELECT DISTINCT STUDENT_GU
FROM 
	dbo.BEPModelsAndHours
WHERE
QUALIFIED = 'N'

) AS T2

ON
T1.STUDENT_GU = T2.STUDENT_GU

