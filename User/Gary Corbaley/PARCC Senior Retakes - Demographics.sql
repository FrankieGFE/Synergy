		WHEN [STUDENT].[RACE_1] = 'White' THEN 'Y'
		WHEN [STUDENT].[RACE_2] = 'White' THEN 'Y'
		WHEN [STUDENT].[RACE_3] = 'White' THEN 'Y'
		WHEN [STUDENT].[RACE_4] = 'White' THEN 'Y'
		WHEN [STUDENT].[RACE_5] = 'White' THEN 'Y'
		ELSE 'N' END AS [White]	
	,'' AS [FILLER]		
	,CASE WHEN [STUDENT].[RACE_2] IS NOT NULL THEN 'Y' ELSE 'N' END AS [Two or More Races]
	
	
