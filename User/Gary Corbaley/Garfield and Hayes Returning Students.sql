



SELECT
	[STUDENT].[SIS_NUMBER]
	,[STUDENT].[FIRST_NAME]
	,[STUDENT].[LAST_NAME]
	,[STUDENT].[MIDDLE_NAME]
	,[2015_ENROLLMENTS].[GRADE]
	,[2015_ENROLLMENTS].[SCHOOL_CODE]
	,[2015_ENROLLMENTS].[SCHOOL_NAME]
	--,[2015_ENROLLMENTS].[ORGANIZATION_GU]
	--,[2015_ENROLLMENTS].[YEAR_GU]
	,CONVERT(VARCHAR(10),[2015_ENROLLMENTS].[ENTER_DATE],101) AS [ENTER_DATE]
	,CONVERT(VARCHAR(10),[2015_ENROLLMENTS].[LEAVE_DATE],101) AS [LEAVE_DATE]
	,[2015_ENROLLMENTS].[LEAVE_CODE]
	,[2015_ENROLLMENTS].[LEAVE_DESCRIPTION]
	
FROM
	APS.PrimaryEnrollmentDetailsAsOf('05/22/2015') [2014_ENROLLMENTS]
	
	INNER JOIN
	APS.PrimaryEnrollmentDetailsAsOf(GETDATE()) [2015_ENROLLMENTS]
	ON
	[2014_ENROLLMENTS].[STUDENT_GU] = [2015_ENROLLMENTS].[STUDENT_GU]
	AND [2014_ENROLLMENTS].[ORGANIZATION_GU] = [2015_ENROLLMENTS].[ORGANIZATION_GU]
	
	INNER JOIN
	APS.BASICSTUDENT AS [STUDENT]
	ON
	[2015_ENROLLMENTS].[STUDENT_GU] = [STUDENT].[STUDENT_GU]
	
WHERE
	[2014_ENROLLMENTS].[SCHOOL_CODE] IN ('410','416')
	AND [2014_ENROLLMENTS].[GRADE] IN ('06','07')
	
ORDER BY
	[2014_ENROLLMENTS].[SCHOOL_NAME]