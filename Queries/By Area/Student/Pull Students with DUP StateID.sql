
SELECT DISTINCT
	STATE_STUDENT_NUMBER
	,IDS

	 FROM

(
SELECT 
	SIS_NUMBER AS SISNBR
	,STATE_STUDENT_NUMBER AS STATEID
	 FROM
REV.EPC_STU
) AS T1

INNER JOIN 
REV.EPC_STU AS T2
ON
t1.STATEID = T2.STATE_STUDENT_NUMBER
AND T1.SISNBR != T2.SIS_NUMBER


UNPIVOT (
IDS FOR SISNUMBERS IN (T1.SISNBR, T2.SIS_NUMBER)
) AS T3

ORDER BY STATE_STUDENT_NUMBER